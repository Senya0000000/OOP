# Определяем функцию find_words_with_sas, которая принимает текстовую строку в качестве аргумента
def find_words_with_sas(text):
    # Разбиваем входную строку на слова, используя пробелы в качестве разделителей
    words = text.split()
    # Создаем пустой список для хранения слов, содержащих подстроку "sas"
    words_with_sas = []
    # Проходимся по каждому слову в списке words
    for word in words:
        # Проверяем, содержит ли текущее слово подстроку "sas"
        if "sas" in word:
            # Если да, добавляем это слово в список words_with_sas
            words_with_sas.append(word)
    # Возвращаем список слов, содержащих "sas"
    return words_with_sas
# Этот блок кода выполняется только в том случае, если данный файл запущен напрямую, а не импортирован как модуль
if __name__ == "__main__":
    # Запрашиваем у пользователя ввод текста и сохраняем его в переменной input_text
    input_text = input("Введите текст: ")
    # Вызываем функцию find_words_with_sas, передавая ей введенный текст
    result = find_words_with_sas(input_text)
    # Проверяем, были ли найдены слова, содержащие "sas"
    if result:
        # Если есть такие слова, выводим заголовок и каждое найденное слово
        print("Слова, содержащие 'sas':")
        for word in result:
            print(word)
    else:
        # Если такие слова не найдены, выводим сообщение об этом
        print("Слова, содержащие 'sas', не найдены.")
